    public void preencheTabela(String where){
        ArrayList<UsuarioModel> rows = CadastroUsuario.getUsers(where);
        
        dados = new DefaultTableModel();
        dados.setNumRows(0);
        dados.addColumn("id");
        dados.addColumn("ra");
        dados.addColumn("email");
        dados.addColumn("nome");
        dados.addColumn("sobrenome");
        
        if(rows != null){
            rows.forEach((usuario) -> {
                dados.addRow(new Object[]{
                    usuario.getId(), 
                    usuario.getRa(), 
                    usuario.getEmail(), 
                    usuario.getNome(), 
                    usuario.getSobrenome()
                });
            });            
        }
        
        tblUsuarios.setModel(dados);
    }
