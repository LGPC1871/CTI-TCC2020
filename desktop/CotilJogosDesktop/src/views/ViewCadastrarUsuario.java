    private void tblUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUsuariosMouseClicked
        int linhaSelecionada = tblUsuarios.getSelectedRow();
        UsuarioModel usuarioSelecionado = new UsuarioModel();
        
        usuarioSelecionado.setRa(Integer.parseInt(tblUsuarios.getValueAt(linhaSelecionada, 1).toString()));
        usuarioSelecionado.setEmail(tblUsuarios.getValueAt(linhaSelecionada, 2).toString());
        usuarioSelecionado.setNome(tblUsuarios.getValueAt(linhaSelecionada, 3).toString());
        usuarioSelecionado.setSobrenome(tblUsuarios.getValueAt(linhaSelecionada, 4).toString());
        
        preencherCampos(usuarioSelecionado);
    }//GEN-LAST:event_tblUsuariosMouseClicked

    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        UsuarioModel usuario = new UsuarioModel();
        if("".equals(txtRa.getText())){
            JOptionPane.showMessageDialog(this, "Preencha todos os campos!", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        usuario.setRa(Integer.parseInt(txtRa.getText()));
        usuario.setEmail(txtEmail.getText());
        usuario.setNome(txtNome.getText());
        usuario.setSobrenome(txtSobrenome.getText());
        
        String result = CadastroUsuario.cadastrarUsuario(usuario);
        System.out.println(result);
        if(result.equals("null")){
            JOptionPane.showMessageDialog(this, "Sucesso!", "Mensagem", WIDTH);
        }else {
            String mensagem;
            switch (result) {
            case "empty":
                mensagem = "Preencha todos os campos!";
                break;
            case "name":
                mensagem = "Nome/Sobrenome inválido(s)!";
                break;
            case "email":
                mensagem = "Email inválido ou já cadastrado!";
                break;
            case "user_invalid":
                mensagem = "RA inválido!";
                break;
            case "user_exist":
                mensagem = "Usuário já cadastrado";
                break;
            default:
                mensagem = "ocorreu um erro";
                break;
            }
            JOptionPane.showMessageDialog(this, mensagem, "Erro", JOptionPane.ERROR_MESSAGE);
        }
        preencheTabela("");
    }//GEN-LAST:event_btnInsertActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        UsuarioModel usuario = new UsuarioModel();
        if(txtRa.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "RA é necessário!", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        usuario.setRa(Integer.parseInt(txtRa.getText()));
        usuario.setEmail(txtEmail.getText());
        usuario.setNome(txtNome.getText());
        usuario.setSobrenome(txtSobrenome.getText());
        
        Boolean resultado = CadastroUsuario.removerUsuario(usuario);
        
        if(resultado){
            JOptionPane.showMessageDialog(this, "Sucesso!", "Mensagem", WIDTH);
        }else{
            JOptionPane.showMessageDialog(this, "Falha ao excluir!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        
        preencheTabela("");
    }//GEN-LAST:event_btnDeleteActionPerformed
    private void preencherCampos(UsuarioModel usuario) {
        txtRa.setText(String.valueOf(usuario.getRa()));
        txtEmail.setText(usuario.getEmail());
        txtNome.setText(usuario.getNome());
        txtSobrenome.setText(usuario.getSobrenome());
    }

